uint8_t CharNweTimesNewRoman21 (uint16_t x, uint16_t y, unsigned char ch, uint16_t CharColor, uint16_t BkgColor)
{
	uint8_t i, j, w, cha, posX=0;
	uint8_t Width = (TimesNewRoman21_Height * TimesNewRoman21_Width)+1;
	uint8_t SymbolWidth=0;
	for (i=1;i<=Width-1;i++)
		{
			for (w=0;w<=TimesNewRoman21_Width-1;w++)
				{
					if (ch<=0x7F)
						{
							cha=TimesNewRoman21[((ch-0x20)*(Width)) + i];
							SymbolWidth=TimesNewRoman21[((ch-0x20)*(Width))];
						}
					if (ch>=0xA0)
						{
							cha=TimesNewRoman21[((ch-0x20-0x20)*(Width)) + i];
							SymbolWidth=TimesNewRoman21[((ch-0x20-0x20)*(Width))];
						}
					if (ch>0x80 && ch<0xA0)
						{
							cha=TimesNewRoman21[(ch-0x20)+i];
							SymbolWidth=TimesNewRoman21[((ch-0x20)*(Width))];
						}
					for (j=0;j<=7;j++)
						{
							if (cha & 0x80)
									Pixel(x+posX,y+j+(w*7)+w,CharColor);
								else
									if (((j+(w*7)+w)<=SymbolWidth)&&(BkgColor!=2))
										Pixel(x+posX,y+j+(w*7)+w,BkgColor);
							cha=cha<<1;
						}
					if (w<TimesNewRoman21_Width-1)
						i++;
				}
			posX++;	
		}
	return SymbolWidth;
}



//-----------------------String---------------------------------
void StringTimesNewRoman21 (uint16_t x, uint16_t y, char *string, uint16_t CharColor, uint16_t BkgColor)
{
	uint8_t SymbolWidth;
	while (*string != 0)
		{
			SymbolWidth=CharNweTimesNewRoman21(x,y,*string,CharColor,BkgColor);
			y=y+SymbolWidth;
			string++;
		}
}


